const generateTimeSlots = ({
  startId,
  fieldId,
  fieldName,
  date,
  dayCategory,
  morningPrice,
  nightPrice,
  bookedTimes = [],
}) => {
  const slots = [];
  let id = startId;

  for (let hour = 7; hour < 24; hour++) {
    const startTime = `${String(hour).padStart(2, "0")}:00`;
    const endTime =
      hour + 1 === 24 ? "00:00" : `${String(hour + 1).padStart(2, "0")}:00`;

    slots.push({
      id: id++,
      fieldId,
      fieldName,
      date,
      dayCategory,
      startTime,
      endTime,
      status: bookedTimes.includes(startTime) ? "Dipesan" : "Tersedia",
      price: hour < 16 ? morningPrice : nightPrice,
    });
  }

  return slots;
};

